package com.ucr.edu.search.eric.pojo;

/**
 *
 * @author Eric Sun
 */
public class ArticlePOJO {

        
    private String url_object_id;
    private String title;
    private String url;
    private String create_date;
    private String front_image_url;
    private String author;
    private String tag;
    private String content;
    private String front_image_path;
    
    public String getUrl_object_id() {
        return url_object_id;
    }

    public void setUrl_object_id(String url_object_id) {
        this.url_object_id = url_object_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getCreate_date() {
        return create_date;
    }

    public void setCreate_date(String create_date) {
        this.create_date = create_date;
    }

    public String getFront_image_url() {
        return front_image_url;
    }

    public void setFront_image_url(String front_image_url) {
        this.front_image_url = front_image_url;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getFront_image_path() {
        return front_image_path;
    }

    public void setFront_image_path(String front_image_path) {
        this.front_image_path = front_image_path;
    }

    
    
}
